        <p>A few months ago we thought it might be a good idea for meetup organisers to get together every so often &mdash; a meetup of meetup organisers. Our plan is simple. To arrange a get together with a bunch of great people. Nothing fancy (organising one meetup is plenty), no talks or schedules, just a nice evening out.</p>
